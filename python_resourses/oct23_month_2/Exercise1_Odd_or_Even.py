# if else odd or even nested statement

number = int(input()) #21

# If the number can be divided by 2 with 0 remainder.
if number %2 ==0:
  print("Even")
else:
  print("Odd")
# = Odd
  
# The % operator is the modulo operator, which gives the remainder of the division. 
# number %2 ==0: gives the remainder when number is divided by 2 is equal to 0. 
# If the condition is true (i.e., number is divisible by 2 with no remainder), meaning number is even, this line will print the string "Even" to the console.
# else: If the condition in the if statement is false (meaning number is odd), This line prints the string "Odd" to the console if the number is odd.